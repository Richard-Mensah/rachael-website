  $body1$## Introduction

When investors speak of "responsible investing," they often invoke ESG scores — ratings that measure a company's performance on Environmental, Social, and Governance criteria. The premise sounds straightforward: companies that treat the planet, people, and their own governance well should, in the long run, outperform those that do not. But do the numbers actually support this claim?

My MSc research at Cardiff Metropolitan University set out to examine precisely this question — focusing on UK FTSE 100 companies and drawing a line between two very different kinds of businesses: those operating in carbon-intensive industries (oil and gas, mining, aviation, chemicals) and those in lower-carbon sectors (technology, financial services, healthcare, retail).

## What the Data Shows

Looking at FTSE 100 companies over a five-year period, my analysis compared ESG ratings against financial performance metrics including return on equity (ROE), return on assets (ROA), earnings per share (EPS), and market capitalisation.

The findings were nuanced — and that nuance matters.

**For low-carbon sector companies**, higher ESG ratings correlated positively with stronger financial performance across most metrics. Technology and financial services firms with strong ESG profiles consistently demonstrated higher market valuations relative to their peers. Investors, it appears, increasingly price in ESG quality for companies where the link between sustainability and business model is direct.

**For carbon-intensive companies**, the picture was more complex. Some high ESG-rated firms in oil and gas — those investing heavily in transition strategies — showed resilience in market valuation even as their sector faced significant headwinds. Others with lower ESG ratings showed short-term profitability that did not translate into sustained market confidence.

## Why the Sector Divide Matters

The most important takeaway from this research is not simply "ESG ratings are good for performance." It is that the relationship between ESG and financial performance is *sector-dependent*.

For carbon-intensive firms, ESG ratings often reflect transition risk management — how well a company is preparing for a low-carbon future. Markets appear to reward companies that take this seriously, even when current profitability remains tied to fossil fuels.

For low-carbon companies, ESG ratings signal operational quality, governance strength, and stakeholder trust — all of which compound into long-term value creation.

## What This Means for Investors and Analysts

Understanding this distinction has practical implications. Analysts applying blanket ESG screens without accounting for sector context may draw incorrect conclusions. A carbon-intensive company with a moderate ESG score undergoing genuine transformation may represent a more interesting opportunity than a low-carbon firm with a high ESG score based primarily on sector classification.

## The Bigger Picture

ESG ratings are imperfect instruments — standardisation across providers remains inconsistent, and the quality of corporate disclosure varies enormously. Nevertheless, the directional signal they provide is real. For FTSE 100 companies, ESG quality is increasingly inseparable from long-term financial performance, particularly as regulatory pressure, investor expectations, and climate risk converge.

My thesis argues that the integration of ESG factors into financial analysis should not be treated as a values exercise separate from traditional finance — it should be treated as a fundamental component of rigorous financial assessment.

